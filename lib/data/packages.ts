// الحصول على باقة بواسطة ID
export function getPackageById(id: string): Package | undefined {
  return PACKAGES.find(pkg => pkg.id === id)
}

// الحصول على كل الباقات
export function getAllPackages(): Package[] {
  return PACKAGES
}

// حساب الدفعة المقدمة (50% دائماً)
export function calculateDeposit(price: number): number {
  return price * 0.5
}

// حساب المبلغ المتبقي (50% دائماً)
export function calculateRemaining(price: number): number {
  return price * 0.5
}

// تنسيق السعر بالجنيه المصري
export function formatEGP(amount: number): string {
  return `${amount.toLocaleString("ar-EG")} ج.م`
}

